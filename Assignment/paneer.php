        <section id="ingredients">
            <h3>Ingredients</h3>
            <ul>
                <li>Paneer (cottage cheese) - 200 grams</li>
                <li>Butter - 2 tablespoons</li>
                <li>Onion - 1 large, finely chopped</li>
                <li>Tomatoes - 2 large, pureed</li>
                <li>Ginger-Garlic paste - 1 tablespoon</li>
                <li>Green chilies - 2, finely chopped</li>
                <li>Cashew nuts - 10-12, soaked and blended into a paste</li>
                <li>Heavy cream - 1/4 cup</li>
                <li>Red chili powder - 1 teaspoon</li>
                <li>Turmeric powder - 1/2 teaspoon</li>
                <li>Coriander powder - 1 teaspoon</li>
                <li>Garam masala - 1/2 teaspoon</li>
                <li>Salt - to taste</li>
                <li>Fresh coriander leaves - for garnish</li>
                <li>Water - as needed</li>
            </ul>
        </section>

        <section id="instructions" class="instructions">
            <h3>Instructions</h3>
            <h5>1. Prepare the Base:</h5>
            <p>Heat 2 tablespoons of butter in a pan over medium heat. Add finely chopped onions and sauté until golden brown. Add the ginger-garlic paste and chopped green chilies. Sauté for a minute until the raw smell disappears.</p>

            <h5>2. Add Spices:</h5>
            <p>Stir in the tomato puree and cook until the oil starts to separate from the mixture. Add red chili powder, turmeric powder, coriander powder, and salt. Mix well and cook for a few more minutes.</p>

            <h5>3. Add Cashew Paste:</h5>
            <p>Add the blended cashew paste to the pan and cook for 2-3 minutes. This adds a creamy texture to the gravy.</p>

            <h5>4. Cook Paneer:</h5>
            <p>Add cubed paneer to the pan and mix gently to coat the paneer with the masala. Pour in water as needed to achieve the desired consistency of the gravy. Simmer for 5-7 minutes.</p>

            <h5>5. Finish with Cream:</h5>
            <p>Stir in the heavy cream and garam masala. Cook for another 2-3 minutes, allowing the flavors to meld together.</p>

            <h5>6. Garnish and Serve:</h5>
            <p>Garnish with fresh coriander leaves. Serve hot with naan, roti, or rice.</p>
        </section>

        <section id="serve">
            <h3>Serving Tips</h3>
            <p>Pair Paneer Butter Masala with soft buttery naan, tandoori roti, or steamed basmati rice for a complete and satisfying meal.</p>
